#include "game.h"
void menu()
{
	printf("******************************\n");
	printf("********    1.play      ******\n");
	printf("********    2.exit      ******\n");
	printf("******************************\n");
}

void game()
{
	char ret = 0;
	char board[ROW][COL] = {0};
	//初始化棋盘的函数
	InitBoard(board, ROW, COL);
	DispalyBoard(board,ROW,COL);
	//下棋
	while (1)
	{
		PlayerMove(board,ROW,COL);
		DispalyBoard(board, ROW, COL);
		//判断输赢
		ret = IsWin(board, ROW, COL);
		if (ret != 'C')
		{
			break;
		}
		ComputerMove(board, ROW, COL);
		DispalyBoard(board, ROW, COL);
		//判断输赢
		ret = IsWin(board, ROW, COL);
		if (ret!='C')
		{
			break;
		}
	}
	if (ret == '*')
	{
		printf("玩家赢\n");
	}
	else if (ret == '#')
	{
		printf("电脑赢\n");
	}
	else
	{
		printf("平局\n");
	}
}

int main()
{
	srand((unsigned int)time(NULL));
	int input=0;
	do
	{
		menu();//打印菜单
		printf("请输入:>");
		scanf("%d",&input);
		switch (input)
		{
		case 1://开始游戏	打印棋盘
			game();
			break;
		case 2://退出
			printf("退出游戏\n");
			break;
		default:printf("错误，请重新输入\n");
			scanf("%d", &input);
			break;
		}
	} while (input==1);
}